// Kiểm tra xem người đó có đủ tuổi để vào câu lạc bộ đêm (21 tuổi)
// Ghi một thông báo phù hợp ra console trong cả hai trường hợp

let age = 22;

// nếu dưới 21 tuổi -> "Bạn không được vào câu lạc bộ!"
// nếu đủ tuổi      -> "Chào mừng!"
if (age < 21) {
    console.log("Bạn không được vào câu lạc bộ!");
} else {
    console.log("Chào mừng!");
}
