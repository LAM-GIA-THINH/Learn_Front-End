// Tạo một biến `bonusPoints`. Khởi tạo nó với giá trị là 50.
// Tăng nó lên 100.
// Giảm xuống 25, sau đó cuối cùng tăng lên 70.
// In giá trị của nó ra console sau mỗi bước.

let bonusPoints = 50; // Khởi tạo với giá trị 50
console.log(bonusPoints); // In ra 50

bonusPoints = bonusPoints+50; // Tăng lên 100
console.log(bonusPoints); // In ra 100

bonusPoints = bonusPoints-75; // Giảm xuống 25
console.log(bonusPoints); // In ra 25

bonusPoints = bonusPoints+45; // Tăng lên 70
